# ═══════════════════════════════════════════════════════════
# Engine Selection Tests (Phase 1A.2 micro-fix)
# ═══════════════════════════════════════════════════════════

class TestEngineSelection:
    """Verify that CitationFormatter routes to the correct engine."""

    def test_auto_gbt_journal_uses_fallback(self, record_cn_single):
        """AUTO + GB/T journal article → fallback, even with citeproc installed."""
        fmt = CitationFormatter(engine=CitationEngine.AUTO)
        cit = fmt.format(record_cn_single, CitationStyle.GBT7714_NUMERIC)
        assert cit.engine_used == "fallback", (
            f"Expected 'fallback', got '{cit.engine_used}'"
        )
        assert cit.strict is True
        assert "UNSUPPORTED" not in cit.formatted

    def test_auto_apa_uses_citeproc_when_available(self, record_en):
        """AUTO + APA → citeproc when available; UNAVAILABLE when not installed."""
        fmt = CitationFormatter(engine=CitationEngine.AUTO)
        cit = fmt.format(record_en, CitationStyle.APA7)
        if HAS_CITEPROC:
            assert cit.engine_used == "citeproc", (
                f"Expected 'citeproc', got '{cit.engine_used}'"
            )
            assert cit.strict is True
        else:
            # Without citeproc → UNAVAILABLE, no silent fallback to unvalidated formatter
            assert cit.engine_used == "", (
                f"Expected empty engine_used (unavailable), got '{cit.engine_used}'"
            )
            assert cit.status == SourceStatus.UNAVAILABLE
            assert cit.strict is False
            assert any("no validated APA fallback" in w for w in cit.warnings)

    def test_force_fallback_journal_article(self, record_cn_single):
        """FALLBACK mode + journal article → fallback."""
        fmt = CitationFormatter(engine=CitationEngine.FALLBACK)
        cit = fmt.format(record_cn_single, CitationStyle.GBT7714_NUMERIC)
        assert cit.engine_used == "fallback"
        assert cit.strict is True
        assert "UNSUPPORTED" not in cit.formatted

    def test_force_fallback_book_returns_unsupported(self):
        """FALLBACK mode + unsupported type → UNSUPPORTED, no citeproc escape."""
        fmt = CitationFormatter(engine=CitationEngine.FALLBACK)
        record = ScholarlyRecord(
            title="A Book", pub_type="book",
            authors=[{"family": "Author", "given": "Name"}], year=2025,
        )
        cit = fmt.format(record, CitationStyle.GBT7714_NUMERIC)
        assert cit.engine_used == "fallback"
        assert "UNSUPPORTED" in cit.formatted
        assert cit.status == SourceStatus.UNAVAILABLE

    def test_force_citeproc_journal_article(self, record_cn_single):
        """CITEPROC mode + journal article → citeproc with strict=False."""
        if not HAS_CITEPROC:
            pytest.skip("citeproc-py not installed")
        fmt = CitationFormatter(engine=CitationEngine.CITEPROC)
