# ═══════════════════════════════════════════════════════════
# GB/T 7714-2015 Golden Tests — Numeric (顺序编码制)
#
# Expected strings are HAND-CRAFTED per GB/T 7714-2015 §8.1.1:
#   作者. 题名[J]. 刊名, 年, 卷(期): 页码.
#
# Rules:
#   - ≤3 authors: list all. >3: first 3 + "等" (Chinese) / "et al" (non-CJK).
#   - Journal name and year separated by ", ".
#   - DOI NOT included in numeric output.
#   - Article number substitutes for page when page is absent.
#   - No volume → issue attaches to year: 年(期): 页码.
# ═══════════════════════════════════════════════════════════

class TestGBT7714NumericGolden:
    """Golden tests for GB/T 7714-2015 numeric style (fallback)."""

    def setup_method(self):
        self.fmt = GBT7714FallbackFormatter()

    def test_golden_cn_single(self, record_cn_single):
        """中文单作者: 作者. 题名[J]. 刊名, 年, 卷(期): 页码."""
        cit = self.fmt.format_numeric(record_cn_single)
        expected = "张三. 平台治理中的算法权力研究[J]. 新闻与传播研究, 2025, 32(4): 15-28."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_cn_three_authors(self, record_cn_three_authors):
        """中文三作者（≤3，全部照录）."""
        cit = self.fmt.format_numeric(record_cn_three_authors)
        expected = "张三, 李四, 王五. 深度学习在自然语言处理中的应用[J]. 中国图书馆学报, 2024, 50(2): 100-120."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_cn_many_authors(self, record_cn_many_authors):
        """中文超过三作者: 前三位 + '等'."""
        cit = self.fmt.format_numeric(record_cn_many_authors)
        expected = "张三, 李四, 王五, 等. 大规模语言模型的知识增强方法研究[J]. 中国图书馆学报, 2024, 50(3): 50-75."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_en_single(self, record_en_single):
        """英文单作者: 姓 名缩写. 题名[J]. 刊名, 年, 卷(期): 页码."""
        cit = self.fmt.format_numeric(record_en_single)
        expected = "Johnson M. Understanding Platform Power Dynamics[J]. Journal of Communication, 2023, 45(2): 200-215."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_en_two_authors(self, record_en):
        """英文双作者: 用缩写."""
        cit = self.fmt.format_numeric(record_en)
        expected = "Smith J, Jones A B. A Survey of Platform Governance Models[J]. Journal of Communication Research, 2023, 40(1): 1-25."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_en_many_authors(self, record_en_many_authors):
        """英文超过三作者: 前三位 + 'et al'."""
        cit = self.fmt.format_numeric(record_en_many_authors)
        expected = "Brown A, Davis R K, Wilson E, et al. Advances in Neural Machine Translation[J]. Computational Linguistics, 2025, 51(1): 1-30."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_mixed_authors(self, record_mixed_authors):
        """中英文混合: 各按规则格式化."""
        cit = self.fmt.format_numeric(record_mixed_authors)
        expected = "张三, Smith J, 李四. Cross-Cultural Study[J]. International Journal of Communication, 2025, 15: 1-20."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_no_volume(self, record_no_volume):
        """缺卷号: 期号直接接年份 — 年(期): 页码."""
        cit = self.fmt.format_numeric(record_no_volume)
        expected = "张三. 某篇缺卷号的文章[J]. 新闻与传播研究, 2025(4): 15-28."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_no_issue(self, record_no_issue):
        """缺期号: 仅卷号 — 年, 卷: 页码."""
        cit = self.fmt.format_numeric(record_no_issue)
        expected = "李四. 缺期号的文章[J]. 情报学报, 2025, 30: 50-60."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_no_page(self, record_no_page):
        """缺页码: 不崩溃，以卷期结尾."""
        cit = self.fmt.format_numeric(record_no_page)
        expected = "王五. 无页码的文章[J]. 新闻与传播研究, 2025, 32(4)."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_article_number(self, record_article_number):
        """Article number 替代页码."""
        cit = self.fmt.format_numeric(record_article_number)
        expected = "Brown C. Article with article number[J]. Physical Review Letters, 2024, 132: 012345."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"

    def test_golden_with_doi(self, record_with_doi):
        """DOI 存在不影响 GB/T numeric 格式化（标准不要求输出 DOI）."""
        cit = self.fmt.format_numeric(record_with_doi)
        expected = "Taylor S. Article With DOI[J]. Nature, 2025, 600: 100-110."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"
        # DOI should NOT appear in the formatted citation
        assert "10.1234" not in cit.formatted, "DOI should not appear in GB/T numeric output"

    def test_golden_year_only(self, record_year_only):
        """仅年份信息（缺卷/期/页码）: 不崩溃，以年份结尾."""
        cit = self.fmt.format_numeric(record_year_only)
        expected = "陈小明. The Future of Digital Governance[J]. 新闻与传播研究, 2025."
        assert cit.formatted == expected, f"\n  Expected: {expected}\n  Got:      {cit.formatted}"


